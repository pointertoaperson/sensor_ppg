void ssd1306_init()
{

    i2c_cmd_handle_t cmd = i2c_cmd_link_create();

    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (OLED_I2C_ADDRESS << 1) | 0, true);
    i2c_master_write_byte(cmd, OLED_CONTROL_BYTE_CMD_STREAM, true);

    i2c_master_write_byte(cmd, 0xAE, true); // Display off
    i2c_master_write_byte(cmd, 0x20, true); // Set Memory Addressing Mode
    i2c_master_write_byte(cmd, 0x00, true); // Horizontal Addressing Mode
    i2c_master_write_byte(cmd, 0xB0, true); // Set Page Start Address
    i2c_master_write_byte(cmd, 0xC8, true); // COM Output Scan Direction (downwards)
    i2c_master_write_byte(cmd, 0x00, true); // Low Column Start Address
    i2c_master_write_byte(cmd, 0x10, true); // High Column Start Address
    i2c_master_write_byte(cmd, 0x40, true); // Start Line Address
    i2c_master_write_byte(cmd, 0x81, true); // Contrast Control
    i2c_master_write_byte(cmd, 0x7F, true);
    i2c_master_write_byte(cmd, 0xA1, true); // Segment Re-map
    i2c_master_write_byte(cmd, 0xA6, true); // Normal Display
    i2c_master_write_byte(cmd, 0xA8, true); // Multiplex Ratio
    i2c_master_write_byte(cmd, 0x1F, true); // 32 rows, so 1F (31 decimal) is appropriate
    i2c_master_write_byte(cmd, 0xA4, true); // Output RAM to Display
    i2c_master_write_byte(cmd, 0xD3, true); // Display Offset
    i2c_master_write_byte(cmd, 0x00, true);
    i2c_master_write_byte(cmd, 0xD5, true); // Display Clock Divide Ratio
    i2c_master_write_byte(cmd, 0xF0, true);
    i2c_master_write_byte(cmd, 0xD9, true); // Pre-charge Period
    i2c_master_write_byte(cmd, 0x22, true);
    i2c_master_write_byte(cmd, 0xDA, true); // COM Pins
    i2c_master_write_byte(cmd, 0x02, true);
    i2c_master_write_byte(cmd, 0xDB, true); // VCOMH Deselect Level
    i2c_master_write_byte(cmd, 0x20, true);
    i2c_master_write_byte(cmd, 0x8D, true); // Charge Pump Setting
    i2c_master_write_byte(cmd, 0x14, true);
    i2c_master_write_byte(cmd, 0xAF, true); // Display ON
    i2c_master_write_byte(cmd, OLED_CMD_DISPLAY_ON, true);

    i2c_master_stop(cmd);

    i2c_master_cmd_begin(I2C_NUM_0, cmd, 10 / portTICK_PERIOD_MS);

    i2c_cmd_link_delete(cmd);
}

void ssd1306_set_address_range(uint8_t start_page, uint8_t end_page)
{
    ssd1306_sendCommand(0x21); // Set column address
    ssd1306_sendCommand(0);    // Start column
    ssd1306_sendCommand(127);  // End column
    ssd1306_sendCommand(0x22); // Set page address
    ssd1306_sendCommand(start_page);
    ssd1306_sendCommand(end_page);
}

