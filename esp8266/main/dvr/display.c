// --- Function to Draw a Pixel ---
void draw_pixel(uint8_t x, uint8_t y)
{
    if (x >= 128 || y >= 32)
        return; // Ensure within bounds
    // y = 31-y ;
    buffer[x + (y / 8) * 128] |= (1 << (y % 8));
}

void draw_waveform(uint16_t *psample, uint16_t data_len)
{
    uint16_t len = 0;
    while (len < data_len)
    {
        uint8_t screen_pos = (len & 0x7F);
        uint16_t y = (*psample);
        y = 31 - y;
        draw_pixel(screen_pos, y + 8);
        len++;
        psample++;
    }
}

